package com.zl.listview;

/**
 * 价格区间
 * 
 * @author xieyw
 * 
 */
public class ItemPriceInBean {

	private String beanPriceIn = "";// 价格区间
	private String beanPrice = "";// 价格

	public String getBeanPriceIn() {
		return beanPriceIn;
	}

	public void setBeanPriceIn(String beanPriceIn) {
		this.beanPriceIn = beanPriceIn;
	}

	public String getBeanPrice() {
		return beanPrice;
	}

	public void setBeanPrice(String beanPrice) {
		this.beanPrice = beanPrice;
	}

}
